import { faNum, formatToman } from "@/lib/utils";

/**
 * قالب پیش‌فرض قرارداد خدمات مشاوره‌ی آرکان (Markdown).
 * موقع ساخت قرارداد، مقادیر طرفین/مبلغ/مدت مستقیم در متن جایگذاری می‌شود؛
 * بعداً در ویرایشگر قابل تغییر است و دکمه‌ی AI هم می‌تواند بازنویسی‌اش کند.
 */

export type ContractContext = {
  clientName: string;
  companyName: string | null;
  dealTitle: string | null;
  amountToman: number;
  startDate: string | null; // YYYY-MM-DD
  durationLabel: string | null;
  challenge?: string | null;
};

function faDate(iso: string | null): string {
  if (!iso) return "................";
  try {
    return new Intl.DateTimeFormat("fa-IR", { dateStyle: "long" }).format(new Date(iso));
  } catch {
    return iso;
  }
}

export function buildDefaultContract(ctx: ContractContext): string {
  const client = ctx.companyName
    ? `${ctx.companyName} (به نمایندگی ${ctx.clientName})`
    : ctx.clientName;
  const subject =
    ctx.dealTitle ?? "ارائه‌ی خدمات مشاوره‌ی استراتژی و رشد کسب‌وکار بر پایه‌ی متدولوژی «چهار رکن»";
  const amount =
    ctx.amountToman > 0
      ? `${formatToman(ctx.amountToman)} (${faNum(ctx.amountToman)} ریال × ۱۰)`
      : "................ تومان";

  return `## ماده ۱ — طرفین قرارداد

این قرارداد فی‌مابین **شرکت مشاوره‌ی مدیریت آرکان** به نشانی تهران (که از این پس «مشاور» نامیده می‌شود) و **${client}** (که از این پس «کارفرما» نامیده می‌شود) منعقد می‌گردد.

## ماده ۲ — موضوع قرارداد

${subject}${ctx.challenge ? `

با تمرکز بر چالش اعلام‌شده‌ی کارفرما: ${ctx.challenge}` : ""}

خدمات در چهار گام متدولوژی آرکان ارائه می‌شود: **شناخت** (تحلیل وضع موجود)، **استراتژی** (طراحی نقشه‌ی راه)، **اجرا** (همراهی در پیاده‌سازی) و **پایش** (سنجش شاخص‌ها و اصلاح مسیر).

## ماده ۳ — مدت قرارداد

مدت اجرای این قرارداد **${ctx.durationLabel ?? "................"}** از تاریخ **${faDate(ctx.startDate)}** است و با توافق کتبی طرفین قابل تمدید می‌باشد.

## ماده ۴ — مبلغ قرارداد و شرایط پرداخت

حق‌الزحمه‌ی مشاور جمعاً **${amount}** است که به شرح زیر پرداخت می‌شود:

- **۴۰٪** هم‌زمان با امضای قرارداد (پیش‌پرداخت)؛
- **۳۰٪** پس از تحویل گزارش فاز شناخت و استراتژی؛
- **۳۰٪** در پایان دوره و پس از ارائه‌ی گزارش نهایی.

## ماده ۵ — تعهدات مشاور

1. ارائه‌ی خدمات با استانداردهای حرفه‌ای و در چارچوب زمان‌بندی توافق‌شده؛
2. معرفی مشاور راهبر مشخص به‌عنوان مسئول پروژه و نقطه‌ی تماس کارفرما؛
3. ارائه‌ی گزارش مکتوب در پایان هر فاز؛
4. رازداری کامل نسبت به اطلاعات کارفرما (ماده ۷).

## ماده ۶ — تعهدات کارفرما

1. در دسترس قراردادن اطلاعات، اسناد و آمار موردنیاز پروژه؛
2. معرفی نماینده‌ی تام‌الاختیار برای هماهنگی و تصمیم‌گیری؛
3. پرداخت به‌موقع مبالغ قرارداد مطابق ماده ۴.

## ماده ۷ — محرمانگی

کلیه‌ی اطلاعات تجاری، مالی و راهبردی که در جریان اجرای این قرارداد بین طرفین مبادله می‌شود محرمانه تلقی شده و افشای آن به اشخاص ثالث بدون اجازه‌ی کتبی طرف دیگر، تا **دو سال** پس از پایان قرارداد ممنوع است.

## ماده ۸ — فسخ قرارداد

هر یک از طرفین می‌تواند با اعلام کتبی **۱۵ روزه** قرارداد را فسخ کند. در صورت فسخ، هزینه‌ی خدمات انجام‌شده تا تاریخ فسخ، محاسبه و تسویه خواهد شد.

## ماده ۹ — حل اختلاف

اختلافات ناشی از تفسیر یا اجرای این قرارداد ابتدا از طریق مذاکره‌ی دوستانه و در صورت عدم توافق، از طریق داوری مرضی‌الطرفین حل‌وفصل می‌شود.

## ماده ۱۰ — نسخ قرارداد

این قرارداد در **۱۰ ماده** تنظیم شده و تأیید آنلاین آن از طریق همین صفحه، به‌منزله‌ی پذیرش کلیه‌ی مفاد آن از سوی کارفرماست.`;
}
